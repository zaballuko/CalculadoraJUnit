package Calculadora;

public class Calculadora {

	public Calculadora() {

	}

	public static int sumar(int x, int y) {
		return x + y;
	}

	public int restar(int x, int y) {
		return x - y;
	}
	public static int multiplicar(int x, double y) {
		
		
		int z = (int) (x * y);
		
		return z;
	}
	
}
